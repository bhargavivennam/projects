package Operations

import "fmt"

// To perform subtraction operation on different datatypes

func SubInt8() {
	var num1 int8
	fmt.Println("Enter first value of type int8 :")
	fmt.Scan(&num1)
	var num2 int8
	fmt.Println("Enter second value of type int8 :")
	fmt.Scan(&num2)
	result1 := num1 - num2
	fmt.Println("The result for subtraction operation on 2 int8 variables is :", result1)

}

